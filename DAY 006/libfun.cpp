#include<iostream>
#include<cmath>
using namespace std;
int main(){
    cout<<"Square root : "<<sqrt(4)<<endl;  // library function
    cout<<"Cube root : "<<cbrt(8)<<endl;
    cout<<"Min value : "<<min(8,4)<<endl;
    cout<<"Max value : "<<max(8,4)<<endl;
    cout<<"Power : "<<pow(4,3)<<endl; // pow(a,b)--> b pow of a
}